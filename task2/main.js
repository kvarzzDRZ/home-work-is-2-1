let temp = 10;

if (temp > 30)
{
    console.log("Вы можете надеть легкую одежду, сегодня будет жарко!");
}
else if (temp > 20)
{
    console.log("Вы можете надеть теплую одежду");
}
else if (temp < 10)
{
    console.log("Вы можете надеть зимнюю одежду, если не хотите простудиться");
}
else
{
    console.log("Можете сами выбрать, что вам одеть");
}